    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        String matric = request.getParameter("matricNo");
    String pass = request.getParameter("password");

    try {
        Class.forName("com.mysql.cj.jdbc.Driver");
        Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3307/librarydb", "root", "");

        String sql = "SELECT * FROM users WHERE matric_no = ? AND password = ?";
        PreparedStatement ps = conn.prepareStatement(sql);
        ps.setString(1, matric);
        ps.setString(2, pass);

        ResultSet rs = ps.executeQuery();
        if (rs.next()) {
            HttpSession session = request.getSession();
            session.setAttribute("userName", rs.getString("name"));
            session.setAttribute("userRole", rs.getString("role"));

            if (rs.getString("role").equals("Admin")) {
                response.sendRedirect("admin_dashboard.jsp");
            } else {
                response.sendRedirect("facility.jsp");
            }
        } else {
            response.sendRedirect("login.jsp?error=failed");
        }
        conn.close();
    } catch (Exception e) { e.printStackTrace(); }
}

